	LIST P = 16F628A
	INCLUDE P16F628A.INC
	
	T1 	EQU		020
	T2 	EQU		021
	T3 	EQU		022

	ORG 	00
	GOTO	INIT
	
	ORG		04
	BTFSS	INTCON,INTF;btfss	portb,0
	BCF		INTCON,INTF	;limpio
	MOVLW	0XAA
	MOVWF	PORTB
	BCF		INTCON,INTF;limpio
INTER:

	BTFSS	INTCON,INTF
	GOTO	INTER
	BCF		INTCON,INTF	
	COMF 	PORTB
	BCF		INTCON,INTF
	GOTO	INTER
	RETFIE

INIT:
	
	CLRF	PORTB
	BANKSEL	TRISB
	MOVLW	0X01
	MOVWF	TRISB		;PIN RB0 DEL PORTB COMO ENTRADA
	BANKSEL	PORTB
	BSF		OPTION_REG,INTEDG		;OPTION_REG,6
	BSF		INTCON,INTE		;OPTION_REG,4
	BSF		INTCON,GIE		;OPTION_REG,7

LOOP:
	GOTO	LOOP
	END